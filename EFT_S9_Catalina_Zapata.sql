-- creacion de vista
CREATE OR REPLACE VIEW VW_ANALISIS_DEUDORES_PERIODO AS
SELECT 

     TO_CHAR(SYN_DEU.NUMRUN, 'FM99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') || '-' || SYN_DEU.DVRUN AS RUT_DEUDOR
,    INITCAP(SYN_DEU.PNOMBRE || ' ' || SYN_DEU.APPATERNO || ' ' || SYN_DEU.APMATERNO) AS "NOMBRE DEUDOR"
,    COUNT(SYN_CUO_TAR.NRO_CUOTA) AS TOTAL_CUOTAS
,    LPAD(TO_CHAR(ROUND(AVG(SYN_CUO_TAR.VALOR_CUOTA)), 'FM9999999999'), 15, ' ') AS PROMEDIO_VALOR_CUOTAS
,    NVL(TO_CHAR(SYN_DEU.FONO_CONTACTO), 'Sin Información') AS "TELEFONO"
,    TO_CHAR(MIN(SYN_CUO_TAR.FECHA_VENC_CUOTA), 'DD-MM-YYYY') AS "FECHA_MAS_ANTIGUA" -- Texto e imagen tenia discrepancias asi que me guio por la figura 2
,    RPAD(UPPER(SYN_OCU.NOMBRE_PROF_OFIC), 20, ' ')AS "OCUPACION"
,    LPAD(TO_CHAR(SYN_TAR_DEU.CUPO_DISP_COMPRA, 'FM9999999999'), 15, ' ') AS CUPO_DISP_COMPRA


    FROM 
        SYN_DEU 
    JOIN 
        SYN_OCU ON SYN_DEU.COD_OCUPACION = SYN_OCU.COD_OCUPACION
    JOIN 
        SYN_TAR_DEU ON SYN_DEU.NUMRUN = SYN_TAR_DEU.NUMRUN
    JOIN 
        SYN_CUO_TAR ON SYN_TAR_DEU.NRO_TARJETA = SYN_CUO_TAR.NRO_TARJETA
        
    WHERE -- Filtros
        UPPER(SYN_OCU.NOMBRE_PROF_OFIC) NOT LIKE '%INGENIERO%' -- excluye ingenieros
        AND EXTRACT(YEAR FROM SYN_CUO_TAR.FECHA_VENC_CUOTA) = EXTRACT(YEAR FROM SYSDATE) - 1 -- "la fecha de la cuota más vencida correspondiente al año anterior"
    
    GROUP BY -- Agrupacion necesaria para las columnas
        SYN_DEU.NUMRUN, 
        SYN_DEU.DVRUN, 
        SYN_DEU.PNOMBRE, 
        SYN_DEU.APPATERNO, 
        SYN_DEU.APMATERNO, 
        SYN_DEU.FONO_CONTACTO,
        SYN_TAR_DEU.CUPO_DISP_COMPRA,
        SYN_OCU.NOMBRE_PROF_OFIC 
    
    HAVING -- Condicion post agrupado "promedio de valor de cuotas es inferior al máximo de todos los promedios de cuotas de las tarjetas de crédito en general"
        AVG(SYN_CUO_TAR.VALOR_CUOTA) < (
            SELECT MAX(promedio_general)
            FROM (
                SELECT AVG(VALOR_CUOTA) as promedio_general
                FROM SYN_CUO_TAR
                GROUP BY NRO_TARJETA
            )
        )
    ORDER BY -- Para ordenar de cantidad de cuotas ascendente y luego por cupo disponible
        TOTAL_CUOTAS ASC, 
        SYN_TAR_DEU.CUPO_DISP_COMPRA ASC;

-- Verificación de las vistas
SELECT * FROM VW_ANALISIS_DEUDORES_PERIODO;

-- Asignacion de privilegios para el usuario consultor
