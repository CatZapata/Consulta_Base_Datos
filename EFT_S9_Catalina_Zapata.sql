-- para comenzar de 0

DROP SEQUENCE SEQ_T_ANALISIS;
DELETE FROM SYN_ANAL_TAR;

-- Creacion de la sequencia con el identificador correlativo NUM_ANALISIS
CREATE SEQUENCE SEQ_T_ANALISIS
    START WITH 1
    INCREMENT BY 1;

-- Sentencia DML para cargar datos en la tabla existente
INSERT INTO SYN_ANAL_TAR (
    NUM_ANALISIS
,   NRO_TARJETA
,   TOTAL_CUOTAS
,   MONTO_TOTAL_TRANSA
,   FECHA_TRANSACCION
,   DIRECCION
,   MONTO_REAJUSTADO
)
-- Uso de secuencia .NEXTVAL se aplica sobre el conjunto de datos ya ordenado
SELECT
    SEQ_T_ANALISIS.NEXTVAL AS NUM_ANALISIS
,   SUBCONSULTA.NRO_TARJETA
,   SUBCONSULTA.TOTAL_CUOTAS
,   SUBCONSULTA.MONTO_TOTAL_TRANSA
,   SUBCONSULTA.FECHA_TRANSACCION
,   SUBCONSULTA.DIRECCION
,   SUBCONSULTA.MONTO_REAJUSTADO
    
FROM (
  -- Subconsulta para procesar filtros y el orden 
    SELECT
        SYN_TRAN_TAR_DEU.NRO_TARJETA AS NRO_TARJETA
,       SYN_TRAN_TAR_DEU.TOTAL_CUOTAS_TRANSACCION AS TOTAL_CUOTAS
,       SYN_TRAN_TAR_DEU. MONTO_TOTAL_TRANSACCION AS MONTO_TOTAL_TRANSA
,       TO_CHAR(SYN_TRAN_TAR_DEU.FECHA_TRANSACCION, 'DD/MM/YYYY') AS FECHA_TRANSACCION
,       INITCAP(SYN_SUC.DIRECCION) AS DIRECCION
,       
        CASE
            WHEN SYN_TRAN_TAR_DEU.MONTO_TOTAL_TRANSACCION BETWEEN 200000 AND 300000
            THEN ROUND(SYN_TRAN_TAR_DEU.MONTO_TOTAL_TRANSACCION * 1.05)
                    
            WHEN SYN_TRAN_TAR_DEU.MONTO_TOTAL_TRANSACCION BETWEEN 300001 AND 500000
                THEN ROUND(SYN_TRAN_TAR_DEU.MONTO_TOTAL_TRANSACCION * 1.07)
                    
            ELSE SYN_TRAN_TAR_DEU.MONTO_TOTAL_TRANSACCION * 1
            
        END AS MONTO_REAJUSTADO
    
        FROM SYN_TRAN_TAR_DEU
    
        JOIN SYN_SUC ON SYN_SUC.ID_SUCURSAL = SYN_TRAN_TAR_DEU.ID_SUCURSAL
    
        WHERE-- Filtro
            UPPER(SYN_SUC.DIRECCION) LIKE 'A%'
            AND SYN_TRAN_TAR_DEU.MONTO_TOTAL_TRANSACCION >= 200000
            
    -- Ordenamiento
        ORDER BY SYN_TRAN_TAR_DEU.NRO_TARJETA ASC, MONTO_REAJUSTADO DESC
) SUBCONSULTA;

-- Confirmacion de cambios 
COMMIT;

-- Verificacion final
SELECT * FROM SYN_ANAL_TAR ORDER BY NRO_TARJETA ASC, MONTO_REAJUSTADO DESC;

-- CREACION DE INDICES

-- Indice para optimizar el filtro de búsqueda por Dirección (LIKE 'A%') Esto evita que Oracle lea toda la tabla SUCURSAL.
CREATE INDEX IDX_SUC_DIRECCION_BUSQ ON PRY2205_EFT.SUCURSAL(DIRECCION);

-- Indice para optimizar el filtro de Montos (>= 200000) Mejora drásticamente el rendimiento de la subconsulta del Caso 3.1.
CREATE INDEX IDX_TRA_MONTO_BUSQ ON PRY2205_EFT.TRANSACCION_TARJETA_DEUDOR(MONTO_TOTAL_TRANSACCION);

-- Indice para optimizar el JOIN entre Transacciones y Sucursales Indexar las llaves foráneas (FK) es una regla de oro en optimización.
CREATE INDEX IDX_TRA_SUCURSAL_FK 
ON PRY2205_EFT.TRANSACCION_TARJETA_DEUDOR(ID_SUCURSAL);

