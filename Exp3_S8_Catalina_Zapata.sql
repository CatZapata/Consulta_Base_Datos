CREATE OR REPLACE VIEW VW_RECALCULO_COSTOS AS
SELECT
    TO_CHAR(PACIENTE.PAC_RUN, 'FM99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') || '-' || PACIENTE.DV_RUN AS RUT_PACIENTE
    ,UPPER(PACIENTE.PNOMBRE || ' ' || PACIENTE.APATERNO || ' ' || PACIENTE.AMATERNO) AS NOMBRE_PACIENTE
    ,SALUD.DESCRIPCION SISTEMA_SALUD
    ,TO_CHAR(BONO_CON.COSTO, '$99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') AS COSTO
    ,BONO_CON.HR_CONSULTA AS HORARIO_ATENCION
    ,TO_CHAR(BONO_CON.FECHA_BONO, 'MM-YYYY') AS FECHA_BONO
    ,TO_CHAR(CASE 
        WHEN BONO_CON.COSTO BETWEEN 15000 AND 25000 THEN ROUND(BONO_CON.COSTO * 1.15)
        WHEN BONO_CON.COSTO > 25000 THEN ROUND(BONO_CON.COSTO * 1.20)
        ELSE BONO_CON.COSTO
    END, '$99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') AS "REAJUSTE"
    FROM PACIENTE
    
    JOIN SALUD
        ON PACIENTE.SAL_ID = SALUD.SAL_ID
        
    JOIN BONO_CON
        ON PACIENTE.PAC_RUN = BONO_CON.PAC_RUN
        
    WHERE 
        BONO_CON.HR_CONSULTA > '17:15'
        AND EXTRACT(YEAR FROM BONO_CON.FECHA_BONO) = EXTRACT(YEAR FROM SYSDATE) - 1
        AND (SALUD.SAL_ID BETWEEN 10 AND 100)
        
    ORDER BY
        BONO_CON.FECHA_BONO ASC,
        PACIENTE.PAC_RUN ASC; -- cuenta de todas maneras los dias en la fecha bono
        
