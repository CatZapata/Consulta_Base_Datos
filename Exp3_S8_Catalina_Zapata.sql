CREATE OR REPLACE VIEW VW_AUM_MEDICO_X_CARGO AS
SELECT 
    MEDICO.RUT_MED || '-' || MEDICO.DV_RUN AS RUT_MEDICO,
    CARGO.NOMBRE AS "CARGO",
    TO_CHAR(MEDICO.SUELDO_BASE, '$99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') AS SUELDO_ACTUAL,
    TO_CHAR(ROUND(MEDICO.SUELDO_BASE * 1.15), '99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') AS SUELDO_AUMENTADO
        FROM MEDICO
        
    JOIN CARGO 
        ON MEDICO.CAR_ID = CARGO.CAR_ID
        
    ORDER BY ROUND(MEDICO.SUELDO_BASE * 1.15) DESC;

