SELECT
    ESPECIALIDAD_MEDICA.NOMBRE AS "ESPECIALIDAD MEDICA"
,   COUNT(BONO_CONSULTA.ID_BONO) AS "CANTIDAD BONOS"
,   TO_CHAR(SUM(BONO_CONSULTA.COSTO), '$99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') AS "MONTO PÉRDIDA"
,   TO_CHAR(MIN(BONO_CONSULTA.FECHA_BONO), 'DD-MM-YYYY') AS "FECHA BONO"
,   CASE   
        WHEN EXTRACT(YEAR FROM MIN(BONO_CONSULTA.FECHA_BONO)) >= EXTRACT(YEAR FROM SYSDATE) - 1 
        THEN 'COBRABLE'
        ELSE 'INCOBRABLE'
    END AS "ESTADO DE COBRO"

FROM BONO_CONSULTA
JOIN ESPECIALIDAD_MEDICA ON BONO_CONSULTA.ESP_ID = ESPECIALIDAD_MEDICA.ESP_ID
WHERE BONO_CONSULTA.ID_BONO IN (
    SELECT ID_BONO FROM BONO_CONSULTA
    MINUS
    SELECT ID_BONO FROM PAGOS )
GROUP BY ESPECIALIDAD_MEDICA.NOMBRE
ORDER BY "CANTIDAD BONOS" ASC, "MONTO PÉRDIDA" DESC;

