DROP TABLE RECAUDACION_BONOS_MEDICOS CASCADE CONSTRAINTS PURGE; -- Elimino la tabla para comenzar limpio

CREATE TABLE RECAUDACION_BONOS_MEDICOS AS

SELECT 
     TO_CHAR(medico.rut_med, 'FM09G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') || '-' || MEDICO.DV_RUN AS "RUT_MEDICO"
,    MEDICO.PNOMBRE || ' ' || MEDICO.APATERNO || ' ' || MEDICO.AMATERNO AS "NOMBRE MÉDICO"
,    TO_CHAR(SUM(BONO_CONSULTA.COSTO), '$99G999G999', 'NLS_NUMERIC_CHARACTERS = '',.''') AS "TOTAL_RECAUDADO"
,    INITCAP(UNIDAD_CONSULTA.NOMBRE) as "UNIDAD_MEDICA"

FROM MEDICO
JOIN BONO_CONSULTA ON MEDICO.rut_med = BONO_CONSULTA.rut_med  -- Llamo a la tabla Bono Consulta
JOIN UNIDAD_CONSULTA ON MEDICO.uni_id = UNIDAD_CONSULTA.uni_id  -- Llamo a la tabla Unidad consulta
WHERE MEDICO.CAR_ID NOT IN (100, 500, 600)   -- Filtro
GROUP BY 
    MEDICO.RUT_MED, 
    MEDICO.DV_RUN, 
    MEDICO.PNOMBRE, 
    MEDICO.APATERNO, 
    MEDICO.AMATERNO,
    UNIDAD_CONSULTA.NOMBRE;
    
SELECT * FROM RECAUDACION_BONOS_MEDICOS  -- Llamo a mi nueva tabla
ORDER BY TOTAL_RECAUDADO ASC; -- Orden ascendente

