DELETE FROM CANT_BONOS_PACIENTES_ANNIO;  -- Borro datos erroneos para empezar bien
COMMIT;

INSERT INTO CANT_BONOS_PACIENTES_ANNIO (ANNIO_CALCULO, PAC_RUN, DV_RUN, EDAD, CANTIDAD_BONOS, MONTO_TOTAL_BONOS, SISTEMA_SALUD)

SELECT 
    EXTRACT(YEAR FROM SYSDATE) AS ANNIO_CALCULO,
    PACIENTE.PAC_RUN,
    PACIENTE.DV_RUN,
    TRUNC(MONTHS_BETWEEN(SYSDATE, PACIENTE.FECHA_NACIMIENTO) / 12) AS EDAD,
    COUNT(BONO_CONSULTA.ID_BONO) AS CANTIDAD_BONOS,
    NVL(SUM(BONO_CONSULTA.COSTO), 0) AS "MONTO_TOTAL_BONOS",
    
    CASE 
        WHEN SALUD.DESCRIPCION IN ('Tramo A', 'Tramo B', 'Tramo C', 'Tramo D')
            THEN 'FONASA'
        
        WHEN SALUD.DESCRIPCION IN ('Mas Vida', 'Colmena', 'Banmedica', 'Cruz Blanca', 'Consalud', 'Vida Tres')
            THEN 'PARTICULARES'
            
        WHEN SALUD.DESCRIPCION IN (
            'Personal activo FFAA',
            'Carga Familar Personal FFAA',
            'Personal Retirado FFAA',
            'Personal activo de carabienros y PDI',
            'Carga Familar Personal Carabineros y PDI',
            'Personal Retirado Carabineros y PDI'
        )
            THEN 'FUERZAS ARMADAS'
            ELSE 'OTRO/NO AFILIADO'
    END AS SISTEMA_SALUD

FROM PACIENTE

LEFT JOIN BONO_CONSULTA ON PACIENTE.PAC_RUN = BONO_CONSULTA.PAC_RUN 
                          AND EXTRACT(YEAR FROM BONO_CONSULTA.FECHA_BONO) = EXTRACT(YEAR FROM SYSDATE)
JOIN SALUD ON PACIENTE.SAL_ID = SALUD.SAL_ID

WHERE SALUD.DESCRIPCION <> 'Personal no afiliado a un sistema de salud'

GROUP BY 
    PACIENTE.PAC_RUN,
    PACIENTE.DV_RUN,
    PACIENTE.FECHA_NACIMIENTO,
    SALUD.DESCRIPCION
    
HAVING 
    NVL(COUNT(BONO_CONSULTA.ID_BONO), 0) <= (
        SELECT ROUND(AVG(COUNT(ID_BONO)))
        FROM BONO_CONSULTA
        WHERE EXTRACT(YEAR FROM FECHA_BONO) = EXTRACT(YEAR FROM SYSDATE) - 1
        GROUP BY PAC_RUN
    );

COMMIT;

SELECT * FROM CANT_BONOS_PACIENTES_ANNIO ORDER BY MONTO_TOTAL_BONOS ASC, EDAD DESC;

